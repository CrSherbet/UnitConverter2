package pa1;
/**
 * This is interface class of unit
 * 
 * @author Warisara
 *
 */
public interface Unit {
	/**
	 * This method will return standard value of the unit
	 * @return standard value of the unit
	 */
	public double getValue();
	
	/**
	 * This method will return output that will show the value and name of unit
	 * @return string of output
	 */
	public String toString();
}
